package app.entities;

public class Message {
  private int id;
  private int from;
  private int to;
  private String body;

  public int getId() {
    return id;
  }

  public int getFrom() {
    return from;
  }

  public int getTo() {
    return to;
  }

  public String getBody() {
    return body;
  }
}
